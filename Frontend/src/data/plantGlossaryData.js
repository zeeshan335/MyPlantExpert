// Get all unique categories
export const glossaryCategories = [
  "All Terms",
  ...new Set(plantGlossary.map((item) => item.category)),
].sort();

// Search function
export const searchGlossary = (searchTerm, language = "english") => {
  const term = searchTerm.toLowerCase().trim();

  return plantGlossary.filter((item) => {
    if (language === "urdu") {
      return item.urduTerm.includes(term) || item.urduDefinition.includes(term);
    }
    return (
      item.term.toLowerCase().includes(term) ||
      item.definition.toLowerCase().includes(term)
    );
  });
};

// Filter by category
export const filterByCategory = (category) => {
  if (category === "All Terms") return plantGlossary;
  return plantGlossary.filter((item) => item.category === category);
};

// Get terms by letter
export const getTermsByLetter = (letter) => {
  return plantGlossary.filter(
    (item) => item.term.charAt(0).toUpperCase() === letter.toUpperCase()
  );
};
