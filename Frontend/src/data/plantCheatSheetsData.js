export const searchCheatSheets = (searchTerm) => {
  const term = searchTerm.toLowerCase().trim();
  return plantCheatSheets.filter(
    (sheet) =>
      sheet.name.toLowerCase().includes(term) ||
      sheet.urduName.includes(term) ||
      sheet.category.toLowerCase().includes(term)
  );
};
